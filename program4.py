# Program 4: Using math operators

# addition
print(2 + 3)
print(2.0 + 3.0)

# Subtraction
print(2.0 - 3.0)
print(2 - 3)

# multiplication
print(2 * 3)

# Division
print(2 / 3)

# integer division
print(2.0 // 3)
print(-6.0 // -3)

# exponentiation
print(2 ** 3.0)

# exponentiation using floating point numbers
print(2 ** 3.0)

# modules
print(2 % 3)